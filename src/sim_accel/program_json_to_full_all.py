def emit_comm_tsv(program: dict[str, object]) -> str:
    vars_data = list(program["vars"])
    lines = ["direction\tslot\tvar_idx\tname\twidth\tis_cpu_visible\n"]
    comm_buffers = program.get("comm_buffers", {})
    if not isinstance(comm_buffers, dict):
        return "".join(lines)
    for direction in ("cpu_to_gpu", "gpu_to_cpu"):
        key = f"{direction}_var_idxs"
        for slot, var_idx in enumerate(_ordered_unique_uint32(comm_buffers.get(key))):
            if not (0 <= var_idx < len(vars_data)):
                continue
            var = vars_data[var_idx]
            lines.append(
                f"{direction}\t{slot}\t{var_idx}\t{var['name']}\t{int(var['width'])}\t"
                f"{_bool_flag(var.get('is_cpu_visible'))}\n"
            )
    return "".join(lines)


