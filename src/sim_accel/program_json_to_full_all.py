class ProgramJsonEmitter:
    def __init__(self, program: dict[str, object]) -> None:
        fmt = str(program.get("format", ""))
        if fmt and not fmt.startswith("sim-accel-program-"):
            raise ValueError(f"Unsupported sim-accel program format: {fmt}")
        self.program = program
        self.vars = list(program.get("vars", []))
        self.exprs = list(program.get("exprs", []))
        self.assigns = list(program.get("assigns", []))
        self.preload_targets = list(program.get("preload_targets", []))
        if not self.vars:
            raise ValueError("Program JSON is missing vars")
        self._expr_cache: dict[int, str] = {}
        self._var_ref_cache: dict[int, list[int]] = {}

    def emit_expr(self, expr_idx: int) -> str:
        cached = self._expr_cache.get(expr_idx)
        if cached is not None:
            return cached
        expr = self.exprs[expr_idx]
        kind = expr["kind"]
        width = int(expr["width"])

        if kind == "var":
            rendered = f"v_{int(expr['var_idx'])}"
        elif kind == "const":
            rendered = _mask_wrap(_unsigned_literal(int(expr["const_value"])), width)
        elif kind == "and":
            rendered = _mask_wrap(f"{self.emit_expr(expr['lhs'])} & {self.emit_expr(expr['rhs'])}", width)
        elif kind == "or":
            rendered = _mask_wrap(f"{self.emit_expr(expr['lhs'])} | {self.emit_expr(expr['rhs'])}", width)
        elif kind == "xor":
            rendered = _mask_wrap(f"{self.emit_expr(expr['lhs'])} ^ {self.emit_expr(expr['rhs'])}", width)
        elif kind == "logand":
            rendered = (
                f"((({self.emit_expr(expr['lhs'])}) != 0ull && "
                f"({self.emit_expr(expr['rhs'])}) != 0ull) ? 1ull : 0ull)"
            )
        elif kind == "logor":
            rendered = (
                f"((({self.emit_expr(expr['lhs'])}) != 0ull || "
                f"({self.emit_expr(expr['rhs'])}) != 0ull) ? 1ull : 0ull)"
            )
        elif kind == "lognot":
            rendered = f"((({self.emit_expr(expr['lhs'])}) == 0ull) ? 1ull : 0ull)"
        elif kind == "add":
            rendered = _mask_wrap(f"{self.emit_expr(expr['lhs'])} + {self.emit_expr(expr['rhs'])}", width)
        elif kind == "sub":
            rendered = _mask_wrap(f"{self.emit_expr(expr['lhs'])} - {self.emit_expr(expr['rhs'])}", width)
        elif kind == "mul":
            rendered = _mask_wrap(
                f"(({self.emit_expr(expr['lhs'])}) * ({self.emit_expr(expr['rhs'])}))", width
            )
        elif kind == "muls":
            lhs = self.exprs[int(expr["lhs"])]
            rhs = self.exprs[int(expr["rhs"])]
            rendered = _mask_wrap(
                "("
                f"static_cast<uint64_t>({_signed_value(self.emit_expr(expr['lhs']), int(lhs['width']))})"
                " * "
                f"static_cast<uint64_t>({_signed_value(self.emit_expr(expr['rhs']), int(rhs['width']))})"
                ")",
                width,
            )
        elif kind == "negate":
            rendered = _mask_wrap(f"(0ull - ({self.emit_expr(expr['lhs'])}))", width)
        elif kind == "eq":
            rendered = f"((({self.emit_expr(expr['lhs'])}) == ({self.emit_expr(expr['rhs'])})) ? 1ull : 0ull)"
        elif kind == "neq":
            rendered = f"((({self.emit_expr(expr['lhs'])}) != ({self.emit_expr(expr['rhs'])})) ? 1ull : 0ull)"
        elif kind == "gt":
            rendered = f"((({self.emit_expr(expr['lhs'])}) > ({self.emit_expr(expr['rhs'])})) ? 1ull : 0ull)"
        elif kind == "gts":
            lhs = self.exprs[int(expr["lhs"])]
            rhs = self.exprs[int(expr["rhs"])]
            rendered = (
                f"(({_signed_value(self.emit_expr(expr['lhs']), int(lhs['width']))} > "
                f"{_signed_value(self.emit_expr(expr['rhs']), int(rhs['width']))}) ? 1ull : 0ull)"
            )
        elif kind == "gte":
            rendered = f"((({self.emit_expr(expr['lhs'])}) >= ({self.emit_expr(expr['rhs'])})) ? 1ull : 0ull)"
        elif kind == "gtes":
            lhs = self.exprs[int(expr["lhs"])]
            rhs = self.exprs[int(expr["rhs"])]
            rendered = (
                f"(({_signed_value(self.emit_expr(expr['lhs']), int(lhs['width']))} >= "
                f"{_signed_value(self.emit_expr(expr['rhs']), int(rhs['width']))}) ? 1ull : 0ull)"
            )
        elif kind == "cond":
            rendered = _mask_wrap(
                f"(({self.emit_expr(expr['lhs'])}) ? ({self.emit_expr(expr['rhs'])}) : ({self.emit_expr(expr['third'])}))",
                width,
            )
        elif kind == "preload_word":
            rendered = _mask_wrap(
                "sim_accel_eval_preload_target_runtime_word_inline("
                f"{int(expr['var_idx'])}u, tid, "
                f"static_cast<uint32_t>((({self.emit_expr(expr['lhs'])}) - ({self.emit_expr(expr['rhs'])})) & 0xffffffffull))",
                width,
            )
        elif kind == "replicate":
            count_expr = self.exprs[int(expr["rhs"])]
            if count_expr["kind"] != "const":
                raise ValueError("replicate count must be a constant in sim_accel.program.json")
            lhs = self.exprs[int(expr["lhs"])]
            rendered = _mask_wrap(
                "sim_accel_replicate_u64("
                f"({self.emit_expr(expr['lhs'])}), {int(lhs['width'])}u, {int(count_expr['const_value'])}u)",
                width,
            )
        elif kind == "concat":
            rhs = self.exprs[int(expr["rhs"])]
            rhs_width = int(rhs["width"])
            rendered = _mask_wrap(
                f"(({self.emit_expr(expr['lhs'])} << {rhs_width}) | "
                f"(({self.emit_expr(expr['rhs'])}) & {_mask_literal(rhs_width)}))",
                width,
            )
        elif kind == "sel":
            lhs = self.exprs[int(expr["lhs"])]
            rendered = _mask_wrap(
                f"((({self.emit_expr(expr['rhs'])}) >= {int(lhs['width'])}u) ? 0ull : "
                f"(({self.emit_expr(expr['lhs'])} >> (({self.emit_expr(expr['rhs'])}) & 63ull)) & {_mask_literal(width)}))",
                width,
            )
        elif kind in {"shiftl", "shiftlovr"}:
            sh = self.emit_expr(expr["rhs"])
            rendered = _mask_wrap(
                f"(({sh} >= 64ull) ? 0ull : ({self.emit_expr(expr['lhs'])} << (({sh}) & 63ull)))",
                width,
            )
        elif kind in {"shiftr", "shiftrovr"}:
            sh = self.emit_expr(expr["rhs"])
            rendered = _mask_wrap(
                f"(({sh} >= 64ull) ? 0ull : ({self.emit_expr(expr['lhs'])} >> (({sh}) & 63ull)))",
                width,
            )
        elif kind in {"shiftrs", "shiftrsovr"}:
            lhs = self.exprs[int(expr["lhs"])]
            zero_on_overshift = "true" if kind == "shiftrsovr" else "false"
            rendered = _mask_wrap(
                "sim_accel_arith_shiftr_u64("
                f"({self.emit_expr(expr['lhs'])}), ({self.emit_expr(expr['rhs'])}), "
                f"{int(lhs['width'])}u, {width}u, {zero_on_overshift})",
                width,
            )
        elif kind == "not":
            rendered = _mask_wrap(f"~{self.emit_expr(expr['lhs'])}", width)
        elif kind in {"ccast", "extend"}:
            rendered = _mask_wrap(self.emit_expr(expr["lhs"]), width)
        elif kind == "extends":
            child = self.exprs[int(expr["lhs"])]
            child_width = int(child["width"])
            if child_width == 0 or child_width >= width:
                rendered = _mask_wrap(self.emit_expr(expr["lhs"]), width)
            else:
                rendered = _mask_wrap(
                    f"(static_cast<uint64_t>({_signed_value(self.emit_expr(expr['lhs']), child_width)}))",
                    width,
                )
        else:
            raise ValueError(f"Unsupported sim-accel expression kind in JSON path: {kind}")

        self._expr_cache[expr_idx] = rendered
        return rendered

    def collect_var_refs(self, expr_idx: int) -> list[int]:
        cached = self._var_ref_cache.get(expr_idx)
        if cached is not None:
            return cached
        expr = self.exprs[expr_idx]
        kind = expr["kind"]
        if kind == "var":
            refs = [int(expr["var_idx"])]
        else:
            refs = []
            for field in ("lhs", "rhs", "third"):
                child = expr.get(field)
                if child is None:
                    continue
                for ref in self.collect_var_refs(int(child)):
                    if ref not in refs:
                        refs.append(ref)
        self._var_ref_cache[expr_idx] = refs
        return refs

    def build_kernel_source(self) -> cuda_fuser.KernelSource:
        declarations: list[cuda_fuser.DeclarationOp] = []
        assignments: list[cuda_fuser.AssignOp] = []
        stores: list[cuda_fuser.StoreOp] = []
        declared_vars: set[int] = set()
        defined_vars: set[int] = set()
        store_order: list[int] = []
        stored_vars: set[int] = set()

        for assign in self.assigns:
            lhs_idx = int(assign["lhs_idx"])
            expr_idx = int(assign["expr_idx"])
            for var_idx in self.collect_var_refs(expr_idx):
                if var_idx in defined_vars or var_idx in declared_vars:
                    continue
                var = self.vars[var_idx]
                declarations.append(
                    cuda_fuser.LoadOp(
                        target=f"v_{var_idx}",
                        source_buffer="state_in",
                        index_expr=f"{var_idx}U * nstates + tid",
                        width_bits=int(var["width"]),
                        comment=str(var["name"]),
                    )
                )
                declared_vars.add(var_idx)
            if lhs_idx not in declared_vars:
                lhs_var = self.vars[lhs_idx]
                declarations.append(
                    cuda_fuser.TempInitOp(
                        target=f"v_{lhs_idx}",
                        value_expr="0ull",
                        comment=f"{lhs_var['name']} (assigned before read)",
                    )
                )
                declared_vars.add(lhs_idx)

            domain = str(assign.get("domain", "combinational"))
            lhs_name = str(assign["lhs_name"])
            assignments.append(
                cuda_fuser.AssignOp(
                    target=f"v_{lhs_idx}",
                    expr=self.emit_expr(expr_idx),
                    comment=f"{lhs_name} ({domain})",
                )
            )
            defined_vars.add(lhs_idx)
            if lhs_idx not in stored_vars:
                stores.append(
                    cuda_fuser.StoreOp(
                        index_expr=f"{lhs_idx}U * nstates + tid",
                        value_expr=f"v_{lhs_idx}",
                        width_bits=int(self.vars[lhs_idx]["width"]),
                    )
                )
                stored_vars.add(lhs_idx)
                store_order.append(lhs_idx)

        del store_order
        prefix = KERNEL_PREFIX_TMPL.format(preload_target_count=len(self.preload_targets))
        return cuda_fuser.KernelSource(
            prefix=prefix,
            phase=cuda_fuser.KernelPhase(
                declarations=declarations,
                assignments=assignments,
                stores=stores,
            ),
        )


def _mlir_int_type(width: int) -> str:
    width = int(width)
    if width <= 0:
        return "i1"
    return f"i{width}"


def _sanitize_mlir_fragment(value: str) -> str:
    out = "".join(ch.lower() if ch.isalnum() else "_" for ch in value)
    out = out.strip("_")
    if not out:
        return "unnamed"
    if out[0].isdigit():
        return f"n_{out}"
    return out


class CirctLikeEmitter:
    """Emit a CIRCT-inspired per-state dataflow dump from sim_accel.program.json.

    The dump uses standard hw/comb syntax where possible and generic
    `"simaccel.*"` operations for semantics that are still SimAccel-specific.
    """

    def __init__(self, program: dict[str, object], source: cuda_fuser.KernelSource) -> None:
        self.program = program
        self.source = source
        self.vars = list(program.get("vars", []))
        self.exprs = list(program.get("exprs", []))
        self.assigns = list(program.get("assigns", []))
        self.input_var_idxs = _extract_source_read_var_indices(source)
        self.output_var_idxs = _extract_source_store_var_indices(source)
        self.temp_inits = self._collect_temp_inits()
        self.const_cache: dict[tuple[int, int], str] = {}
        self.current_values: dict[int, str] = {}
        self.var_versions: Counter[int] = Counter()
        self.temp_index = 0
        self.body_lines: list[str] = []

    def _collect_temp_inits(self) -> dict[int, int]:
        temp_inits: dict[int, int] = {}
        for declaration in self.source.phase.declarations:
            if not isinstance(declaration, cuda_fuser.TempInitOp):
                continue
            target = declaration.target
            if not target.startswith("v_"):
                continue
            idx_text = target[2:]
            if not idx_text.isdigit():
                continue
            temp_inits[int(idx_text)] = 0
        return temp_inits

    def _fresh(self, prefix: str) -> str:
        name = f"%{_sanitize_mlir_fragment(prefix)}_{self.temp_index}"
        self.temp_index += 1
        return name

    def _emit(self, text: str) -> None:
        self.body_lines.append(f"    {text}\n")

    def _emit_comment(self, text: str) -> None:
        self.body_lines.append(f"    // {text}\n")

    def _emit_const(self, value: int, width: int) -> str:
        width = max(1, int(width))
        masked = int(value) & _mask_value(width)
        key = (width, masked)
        cached = self.const_cache.get(key)
        if cached is not None:
            return cached
        name = self._fresh(f"c{masked}_{width}")
        self._emit(f"{name} = hw.constant {masked} : {_mlir_int_type(width)}")
        self.const_cache[key] = name
        return name

    def _emit_comb_binary(self, opname: str, lhs: str, rhs: str, width: int) -> str:
        name = self._fresh(opname.split(".")[-1])
        self._emit(f"{name} = {opname} {lhs}, {rhs} : {_mlir_int_type(width)}")
        return name

    def _emit_icmp(self, pred: str, lhs: str, rhs: str, width: int) -> str:
        name = self._fresh(f"icmp_{pred}")
        self._emit(f"{name} = comb.icmp {pred} {lhs}, {rhs} : {_mlir_int_type(width)}")
        return name

    def _emit_mux(self, cond: str, on_true: str, on_false: str, width: int) -> str:
        name = self._fresh("mux")
        self._emit(f"{name} = comb.mux {cond}, {on_true}, {on_false} : {_mlir_int_type(width)}")
        return name

    def _emit_extract(self, value: str, from_width: int, low_bit: int, out_width: int) -> str:
        name = self._fresh("extract")
        if out_width == 1:
            self._emit(
                f"{name} = comb.extract {value} from {low_bit} : ({_mlir_int_type(from_width)}) -> i1"
            )
        else:
            self._emit(
                f"{name} = comb.extract {value} from {low_bit} : ({_mlir_int_type(from_width)}) -> {_mlir_int_type(out_width)}"
            )
        return name

    def _emit_concat(self, operands: list[str], widths: list[int]) -> str:
        name = self._fresh("concat")
        types = ", ".join(_mlir_int_type(width) for width in widths)
        ops = ", ".join(operands)
        self._emit(f"{name} = comb.concat {ops} : {types}")
        return name

    def _emit_replicate(self, value: str, in_width: int, out_width: int) -> str:
        name = self._fresh("replicate")
        self._emit(
            f"{name} = comb.replicate {value} : ({_mlir_int_type(in_width)}) -> {_mlir_int_type(out_width)}"
        )
        return name

    def _emit_generic_simaccel_op(
        self,
        opname: str,
        operands: list[str],
        operand_widths: list[int],
        result_width: int,
        attrs: dict[str, str],
    ) -> str:
        name = self._fresh(opname.replace(".", "_"))
        attr_text = ""
        if attrs:
            attr_text = " {" + ", ".join(f"{key} = {value}" for key, value in attrs.items()) + "}"
        operand_types = ", ".join(_mlir_int_type(width) for width in operand_widths)
        operands_text = ", ".join(operands)
        self._emit(
            f'{name} = "{opname}"({operands_text}){attr_text} : ({operand_types}) -> {_mlir_int_type(result_width)}'
        )
        return name

    def _emit_truthy(self, value: str, width: int) -> str:
        if width == 1:
            return value
        zero = self._emit_const(0, width)
        return self._emit_icmp("ne", value, zero, width)

    def _emit_resize_if_needed(
        self,
        value: str,
        in_width: int,
        out_width: int,
        *,
        signed: bool,
    ) -> str:
        if int(in_width) == int(out_width):
            return value
        return self._emit_resize(value, in_width, out_width, signed=signed)

    def _emit_resize(self, value: str, in_width: int, out_width: int, *, signed: bool) -> str:
        in_width = int(in_width)
        out_width = int(out_width)
        if in_width == out_width:
            return value
        if in_width > out_width:
            return self._emit_extract(value, in_width, 0, out_width)
        extra_width = out_width - in_width
        if signed:
            sign_bit = self._emit_extract(value, in_width, in_width - 1, 1)
            prefix = sign_bit if extra_width == 1 else self._emit_replicate(sign_bit, 1, extra_width)
        else:
            prefix = self._emit_const(0, extra_width)
        return self._emit_concat([prefix, value], [extra_width, in_width])

    def _emit_u64_carried_shift(
        self,
        *,
        lhs_idx: int,
        rhs_idx: int,
        lhs_guard_limit: int,
        result_width: int,
        cache: dict[int, str],
        shift_op: str,
    ) -> str:
        lhs = self.exprs[lhs_idx]
        rhs = self.exprs[rhs_idx]
        lhs_width = int(lhs["width"])
        rhs_width = int(rhs["width"])
        carrier_width = 64
        lhs_value = self._emit_resize_if_needed(
            self._lower_expr(lhs_idx, cache),
            lhs_width,
            carrier_width,
            signed=False,
        )
        rhs_value = self._lower_expr(rhs_idx, cache)
        shift_amount = self._emit_resize_if_needed(
            rhs_value,
            rhs_width,
            carrier_width,
            signed=False,
        )
        shift_mask = self._emit_const(63, carrier_width)
        masked_shift = self._emit_comb_binary(
            "comb.and",
            shift_amount,
            shift_mask,
            carrier_width,
        )
        shifted_full = self._emit_comb_binary(
            shift_op,
            lhs_value,
            masked_shift,
            carrier_width,
        )
        shifted = self._emit_resize_if_needed(
            shifted_full,
            carrier_width,
            int(result_width),
            signed=False,
        )
        limit = self._emit_const(lhs_guard_limit, carrier_width)
        in_range = self._emit_icmp(
            "ult",
            shift_amount,
            limit,
            carrier_width,
        )
        zero = self._emit_const(0, int(result_width))
        return self._emit_mux(in_range, shifted, zero, int(result_width))

    def _coerce_rhs_to_var_width(self, var_idx: int, rhs: str, rhs_width: int | None = None) -> str:
        if rhs_width is None:
            return rhs
        return self._emit_resize_if_needed(
            rhs,
            int(rhs_width),
            int(self.vars[var_idx]["width"]),
            signed=False,
        )

    def _emit_var_wire(self, var_idx: int, rhs: str, rhs_width: int | None = None) -> str:
        rhs = self._coerce_rhs_to_var_width(var_idx, rhs, rhs_width)
        version = self.var_versions[var_idx]
        self.var_versions[var_idx] += 1
        name = f"%v{var_idx}_{version}"
        self._emit(f"{name} = hw.wire {rhs} : {_mlir_int_type(int(self.vars[var_idx]['width']))}")
        self.current_values[var_idx] = name
        return name

    def _emit_preload_word_expr(self, expr: dict[str, object], cache: dict[int, str], width: int) -> str:
        lhs_idx = int(expr["lhs"])
        rhs_idx = int(expr["rhs"])
        return self._emit_generic_simaccel_op(
            "simaccel.preload_word",
            [
                self._lower_expr(lhs_idx, cache),
                self._lower_expr(rhs_idx, cache),
            ],
            [int(self.exprs[lhs_idx]["width"]), int(self.exprs[rhs_idx]["width"])],
            width,
            {
                "target": f"{int(expr['var_idx'])} : i32",
            },
        )

    def _lower_expr(self, expr_idx: int, cache: dict[int, str]) -> str:
        cached = cache.get(expr_idx)
        if cached is not None:
            return cached
        expr = self.exprs[expr_idx]
        kind = str(expr["kind"])
        width = int(expr["width"])

        if kind == "var":
            var_idx = int(expr["var_idx"])
            raw = self.current_values[var_idx]
            raw_width = int(self.vars[var_idx]["width"])
            value = self._emit_resize_if_needed(raw, raw_width, width, signed=False)
        elif kind == "const":
            value = self._emit_const(int(expr["const_value"]), width)
        elif kind in {"and", "or", "xor", "add", "sub", "mul", "muls"}:
            opname = {
                "and": "comb.and",
                "or": "comb.or",
                "xor": "comb.xor",
                "add": "comb.add",
                "sub": "comb.sub",
                "mul": "comb.mul",
                "muls": "comb.mul",
            }[kind]
            lhs_idx = int(expr["lhs"])
            rhs_idx = int(expr["rhs"])
            lhs_expr = self.exprs[lhs_idx]
            rhs_expr = self.exprs[rhs_idx]
            signed = kind == "muls"
            lhs = self._emit_resize_if_needed(
                self._lower_expr(lhs_idx, cache),
                int(lhs_expr["width"]),
                width,
                signed=signed,
            )
            rhs = self._emit_resize_if_needed(
                self._lower_expr(rhs_idx, cache),
                int(rhs_expr["width"]),
                width,
                signed=signed,
            )
            value = self._emit_comb_binary(
                opname,
                lhs,
                rhs,
                width,
            )
        elif kind == "negate":
            zero = self._emit_const(0, width)
            child_idx = int(expr["lhs"])
            child_expr = self.exprs[child_idx]
            child = self._emit_resize_if_needed(
                self._lower_expr(child_idx, cache),
                int(child_expr["width"]),
                width,
                signed=False,
            )
            value = self._emit_comb_binary(
                "comb.sub", zero, child, width
            )
        elif kind in {"eq", "neq", "gt", "gte", "gts", "gtes"}:
            pred = {
                "eq": "eq",
                "neq": "ne",
                "gt": "ugt",
                "gte": "uge",
                "gts": "sgt",
                "gtes": "sge",
            }[kind]
            lhs_idx = int(expr["lhs"])
            rhs_idx = int(expr["rhs"])
            lhs_expr = self.exprs[lhs_idx]
            rhs_expr = self.exprs[rhs_idx]
            cmp_width = max(int(lhs_expr["width"]), int(rhs_expr["width"]))
            signed = kind in {"gts", "gtes"}
            lhs = self._emit_resize_if_needed(
                self._lower_expr(lhs_idx, cache),
                int(lhs_expr["width"]),
                cmp_width,
                signed=signed,
            )
            rhs = self._emit_resize_if_needed(
                self._lower_expr(rhs_idx, cache),
                int(rhs_expr["width"]),
                cmp_width,
                signed=signed,
            )
            value = self._emit_icmp(
                pred,
                lhs,
                rhs,
                cmp_width,
            )
        elif kind in {"logand", "logor"}:
            lhs_expr = self.exprs[int(expr["lhs"])]
            rhs_expr = self.exprs[int(expr["rhs"])]
            lhs = self._emit_truthy(self._lower_expr(int(expr["lhs"]), cache), int(lhs_expr["width"]))
            rhs = self._emit_truthy(self._lower_expr(int(expr["rhs"]), cache), int(rhs_expr["width"]))
            value = self._emit_comb_binary(
                "comb.and" if kind == "logand" else "comb.or",
                lhs,
                rhs,
                1,
            )
        elif kind == "lognot":
            child = self.exprs[int(expr["lhs"])]
            zero = self._emit_const(0, int(child["width"]))
            value = self._emit_icmp(
                "eq",
                self._lower_expr(int(expr["lhs"]), cache),
                zero,
                int(child["width"]),
            )
        elif kind == "cond":
            cond_expr = self.exprs[int(expr["lhs"])]
            true_idx = int(expr["rhs"])
            false_idx = int(expr["third"])
            true_expr = self.exprs[true_idx]
            false_expr = self.exprs[false_idx]
            cond = self._emit_truthy(self._lower_expr(int(expr["lhs"]), cache), int(cond_expr["width"]))
            on_true = self._emit_resize_if_needed(
                self._lower_expr(true_idx, cache),
                int(true_expr["width"]),
                width,
                signed=False,
            )
            on_false = self._emit_resize_if_needed(
                self._lower_expr(false_idx, cache),
                int(false_expr["width"]),
                width,
                signed=False,
            )
            value = self._emit_mux(
                cond,
                on_true,
                on_false,
                width,
            )
        elif kind == "preload_word":
            value = self._emit_preload_word_expr(expr, cache, width)
        elif kind == "replicate":
            lhs = self.exprs[int(expr["lhs"])]
            count_expr = self.exprs[int(expr["rhs"])]
            if count_expr["kind"] != "const":
                value = self._emit_generic_simaccel_op(
                    "simaccel.replicate",
                    [self._lower_expr(int(expr["lhs"]), cache), self._lower_expr(int(expr["rhs"]), cache)],
                    [int(lhs["width"]), int(count_expr["width"])],
                    width,
                    {},
                )
            else:
                value = self._emit_replicate(
                    self._lower_expr(int(expr["lhs"]), cache),
                    int(lhs["width"]),
                    width,
                )
        elif kind == "concat":
            lhs_idx = int(expr["lhs"])
            rhs_idx = int(expr["rhs"])
            lhs = self.exprs[lhs_idx]
            rhs = self.exprs[rhs_idx]
            value = self._emit_concat(
                [self._lower_expr(lhs_idx, cache), self._lower_expr(rhs_idx, cache)],
                [int(lhs["width"]), int(rhs["width"])],
            )
        elif kind == "sel":
            lhs_idx = int(expr["lhs"])
            value = self._emit_u64_carried_shift(
                lhs_idx=lhs_idx,
                rhs_idx=int(expr["rhs"]),
                lhs_guard_limit=int(self.exprs[lhs_idx]["width"]),
                result_width=width,
                cache=cache,
                shift_op="comb.shru",
            )
        elif kind in {"shiftl", "shiftlovr", "shiftr", "shiftrovr"}:
            value = self._emit_u64_carried_shift(
                lhs_idx=int(expr["lhs"]),
                rhs_idx=int(expr["rhs"]),
                lhs_guard_limit=64,
                result_width=width,
                cache=cache,
                shift_op="comb.shl" if kind in {"shiftl", "shiftlovr"} else "comb.shru",
            )
        elif kind in {"shiftrs", "shiftrsovr"}:
            lhs_idx = int(expr["lhs"])
            rhs_idx = int(expr["rhs"])
            lhs = self.exprs[lhs_idx]
            rhs = self.exprs[rhs_idx]
            value = self._emit_generic_simaccel_op(
                "simaccel.shrs",
                [self._lower_expr(lhs_idx, cache), self._lower_expr(rhs_idx, cache)],
                [int(lhs["width"]), int(rhs["width"])],
                width,
                {
                    "zero_on_overshift": "true" if kind == "shiftrsovr" else "false",
                    "value_width": f"{int(lhs['width'])} : i32",
                },
            )
        elif kind == "not":
            ones = self._emit_const(_mask_value(width), width)
            child_idx = int(expr["lhs"])
            child_expr = self.exprs[child_idx]
            child = self._emit_resize_if_needed(
                self._lower_expr(child_idx, cache),
                int(child_expr["width"]),
                width,
                signed=False,
            )
            value = self._emit_comb_binary(
                "comb.xor", child, ones, width
            )
        elif kind in {"ccast", "extend"}:
            child = self.exprs[int(expr["lhs"])]
            value = self._emit_resize(
                self._lower_expr(int(expr["lhs"]), cache),
                int(child["width"]),
                width,
                signed=False,
            )
        elif kind == "extends":
            child = self.exprs[int(expr["lhs"])]
            value = self._emit_resize(
                self._lower_expr(int(expr["lhs"]), cache),
                int(child["width"]),
                width,
                signed=True,
            )
        else:
            operand_exprs = [
                int(expr[field])
                for field in ("lhs", "rhs", "third")
                if expr.get(field) is not None
            ]
            value = self._emit_generic_simaccel_op(
                f"simaccel.{kind}",
                [self._lower_expr(idx, cache) for idx in operand_exprs],
                [int(self.exprs[idx]["width"]) for idx in operand_exprs],
                width,
                {},
            )

        cache[expr_idx] = value
        return value

    def emit(self) -> str:
        input_ports = [
            f"in %in_v{var_idx}: {_mlir_int_type(int(self.vars[var_idx]['width']))}"
            for var_idx in self.input_var_idxs
        ]
        output_ports = [
            f"out out_v{var_idx}: {_mlir_int_type(int(self.vars[var_idx]['width']))}"
            for var_idx in self.output_var_idxs
        ]
        signature = ", ".join(input_ports + output_ports)
        lines = [
            "// Generated by program_json_to_full_all.py\n",
            "// CIRCT-inspired per-state dataflow dump.\n",
            "// Standard hw/comb operations are emitted directly; SimAccel-only semantics\n",
            '// remain as generic "simaccel.*" operations.\n',
            "module {\n",
            f"  hw.module @sim_accel_eval_full_all_state({signature}) {{\n",
            f"    // expr_count = {len(self.exprs)}, assign_count = {len(self.assigns)}\n",
        ]
        for var_idx in self.input_var_idxs:
            var = self.vars[var_idx]
            self._emit_comment(
                f"input v{var_idx} name={var['name']} width={int(var['width'])} domain=state_in"
            )
            self.current_values[var_idx] = f"%in_v{var_idx}"
        for var_idx, init_value in sorted(self.temp_inits.items()):
            zero = self._emit_const(init_value, int(self.vars[var_idx]["width"]))
            self._emit_var_wire(var_idx, zero)
        for assign_index, assign in enumerate(self.assigns):
            lhs_idx = int(assign["lhs_idx"])
            self._emit_comment(
                f"assign[{assign_index}] {assign['lhs_name']} ({assign.get('domain', 'combinational')})"
            )
            cache: dict[int, str] = {}
            rhs = self._lower_expr(int(assign["expr_idx"]), cache)
            self._emit_var_wire(lhs_idx, rhs, int(self.exprs[int(assign["expr_idx"])] ["width"]))
        lines.extend(self.body_lines)
        output_values: list[str] = []
        output_types: list[str] = []
        for var_idx in self.output_var_idxs:
            output_values.append(self.current_values[var_idx])
            output_types.append(_mlir_int_type(int(self.vars[var_idx]["width"])))
        outputs_text = ", ".join(output_values) if output_values else ""
        output_types_text = ", ".join(output_types) if output_types else ""
        if output_values:
            lines.append(f"    hw.output {outputs_text} : {output_types_text}\n")
        else:
            lines.append("    hw.output\n")
        lines.append("  }\n")
        lines.append("}\n")
        return "".join(lines)


def emit_circt_like_mlir(program: dict[str, object], source: cuda_fuser.KernelSource) -> str:
    return CirctLikeEmitter(program, source).emit()


class CirctFuncEmitter(CirctLikeEmitter):
    """Emit a func.func+comb form that CIRCT can lower to LLVM dialect."""

    PRELOAD_HELPER_NAME = "sim_accel_eval_preload_target_runtime_word_inline"

    def __init__(self, program: dict[str, object], source: cuda_fuser.KernelSource) -> None:
        super().__init__(program, source)
        self.generic_op_count = 0
        self.state_index_arg = "%state_index"
        self.has_preload_word = any(str(expr.get("kind")) == "preload_word" for expr in self.exprs)

    def _emit_var_wire(self, var_idx: int, rhs: str, rhs_width: int | None = None) -> str:
        self.current_values[var_idx] = self._coerce_rhs_to_var_width(var_idx, rhs, rhs_width)
        return self.current_values[var_idx]

    def _emit_func_call(
        self,
        callee: str,
        operands: list[str],
        operand_widths: list[int],
        result_width: int,
    ) -> str:
        name = self._fresh(callee)
        operand_types = ", ".join(_mlir_int_type(width) for width in operand_widths)
        operands_text = ", ".join(operands)
        self._emit(
            f"{name} = func.call @{callee}({operands_text}) : ({operand_types}) -> {_mlir_int_type(result_width)}"
        )
        return name

    def _emit_preload_word_expr(self, expr: dict[str, object], cache: dict[int, str], width: int) -> str:
        lhs_idx = int(expr["lhs"])
        rhs_idx = int(expr["rhs"])
        lhs_width = int(self.exprs[lhs_idx]["width"])
        rhs_width = int(self.exprs[rhs_idx]["width"])
        arith_width = max(32, lhs_width, rhs_width)
        lhs = self._emit_resize_if_needed(
            self._lower_expr(lhs_idx, cache),
            lhs_width,
            arith_width,
            signed=False,
        )
        rhs = self._emit_resize_if_needed(
            self._lower_expr(rhs_idx, cache),
            rhs_width,
            arith_width,
            signed=False,
        )
        diff = self._emit_comb_binary("comb.sub", lhs, rhs, arith_width)
        word_index = self._emit_resize_if_needed(diff, arith_width, 32, signed=False)
        target = self._emit_const(int(expr["var_idx"]), 32)
        loaded = self._emit_func_call(
            self.PRELOAD_HELPER_NAME,
            [target, self.state_index_arg, word_index],
            [32, 32, 32],
            64,
        )
        return self._emit_resize_if_needed(loaded, 64, width, signed=False)

    def _emit_generic_simaccel_op(
        self,
        opname: str,
        operands: list[str],
        operand_widths: list[int],
        result_width: int,
        attrs: dict[str, str],
    ) -> str:
        self.generic_op_count += 1
        return super()._emit_generic_simaccel_op(
            opname,
            operands,
            operand_widths,
            result_width,
            attrs,
        )

    def emit(self) -> str:
        input_ports = [
            f"%in_v{var_idx}: {_mlir_int_type(int(self.vars[var_idx]['width']))}"
            for var_idx in self.input_var_idxs
        ]
        input_ports.append(f"{self.state_index_arg}: i32")
        output_types = [
            _mlir_int_type(int(self.vars[var_idx]["width"])) for var_idx in self.output_var_idxs
        ]
        signature = ", ".join(input_ports)
        if output_types:
            result_sig = " -> " + (
                output_types[0] if len(output_types) == 1 else f"({', '.join(output_types)})"
            )
        else:
            result_sig = ""
        lines = [
            "// Generated by program_json_to_full_all.py\n",
            "// CIRCT func+comb form intended for --convert-to-llvm.\n",
            "module {\n",
        ]
        if self.has_preload_word:
            lines.append(
                f"  func.func private @{self.PRELOAD_HELPER_NAME}(i32, i32, i32) -> i64\n"
            )
        lines.extend(
            [
                f"  func.func @sim_accel_eval_full_all_state_func({signature}){result_sig} {{\n",
                f"    // expr_count = {len(self.exprs)}, assign_count = {len(self.assigns)}\n",
            ]
        )
        for var_idx in self.input_var_idxs:
            var = self.vars[var_idx]
            self._emit_comment(
                f"input v{var_idx} name={var['name']} width={int(var['width'])} domain=state_in"
            )
            self.current_values[var_idx] = f"%in_v{var_idx}"
        for var_idx, init_value in sorted(self.temp_inits.items()):
            zero = self._emit_const(init_value, int(self.vars[var_idx]["width"]))
            self.current_values[var_idx] = zero
        for assign_index, assign in enumerate(self.assigns):
            lhs_idx = int(assign["lhs_idx"])
            self._emit_comment(
                f"assign[{assign_index}] {assign['lhs_name']} ({assign.get('domain', 'combinational')})"
            )
            cache: dict[int, str] = {}
            rhs = self._lower_expr(int(assign["expr_idx"]), cache)
            self.current_values[lhs_idx] = self._coerce_rhs_to_var_width(
                lhs_idx,
                rhs,
                int(self.exprs[int(assign["expr_idx"])]["width"]),
            )
        lines.extend(self.body_lines)
        output_values: list[str] = [self.current_values[var_idx] for var_idx in self.output_var_idxs]
        if output_values:
            if len(output_values) == 1:
                lines.append(f"    return {output_values[0]} : {output_types[0]}\n")
            else:
                lines.append(
                    f"    return {', '.join(output_values)} : {', '.join(output_types)}\n"
                )
        else:
            lines.append("    return\n")
        lines.append("  }\n")
        lines.append("}\n")
        return "".join(lines)


def emit_circt_func_mlir(program: dict[str, object], source: cuda_fuser.KernelSource) -> tuple[str, int]:
    emitter = CirctFuncEmitter(program, source)
    return emitter.emit(), emitter.generic_op_count


def _llvm_ir_int_type(width: int) -> str:
    return f"i{max(1, int(width))}"


def _llvm_ir_return_type(widths: list[int]) -> str:
    if not widths:
        return "void"
    if len(widths) == 1:
        return _llvm_ir_int_type(widths[0])
    return "{ " + ", ".join(_llvm_ir_int_type(width) for width in widths) + " }"


def _extract_llvm_function_definition(llvm_ir: str, func_name: str) -> str:
    match = re.search(
        rf"(?ms)^define\s+.*?@{re.escape(func_name)}\b.*?\n\}}",
        llvm_ir,
    )
    if match is None:
        raise RuntimeError(f"Could not locate translated LLVM function: {func_name}")
    return match.group(0).strip() + "\n"


def _emit_circt_preload_runtime_llvm_support(program: dict[str, object]) -> str:
    exprs = list(program.get("exprs", []))
    if not any(str(expr.get("kind")) == "preload_word" for expr in exprs):
        return ""
    target_count = int(len(list(program.get("preload_targets", []))))
    array_size = max(1, target_count)
    lines: list[str] = []
    lines.append("%SimAccelPreloadTargetView = type { ptr addrspace(1), i64, i32, i32 }\n")
    # The CIRCT/PTX path currently emits a standalone validation artifact rather than
    # linking against the generated CUDA runtime TU, so it owns its own device-side
    # preload constants here instead of leaving them unresolved.
    lines.append(
        "@sim_accel_eval_preload_runtime_nstates_device = addrspace(4) global i32 0, align 4\n"
    )
    lines.append(
        f"@sim_accel_eval_preload_target_views_device = addrspace(4) global [{array_size} x %SimAccelPreloadTargetView] zeroinitializer, align 8\n\n"
    )
    lines.append(
        "define i64 @sim_accel_eval_preload_target_runtime_word_inline(i32 %index, i32 %state_index, i32 %word_index) {\n"
    )
    lines.append("entry:\n")
    lines.append(
        "  %nstates = load i32, ptr addrspace(4) @sim_accel_eval_preload_runtime_nstates_device, align 4\n"
    )
    lines.append("  %state_oob = icmp uge i32 %state_index, %nstates\n")
    lines.append("  br i1 %state_oob, label %ret_zero, label %check_index\n\n")
    lines.append("check_index:\n")
    lines.append(f"  %index_oob = icmp uge i32 %index, {target_count}\n")
    lines.append("  br i1 %index_oob, label %ret_zero, label %load_target\n\n")
    lines.append("load_target:\n")
    lines.append(
        f"  %target_ptr = getelementptr [{array_size} x %SimAccelPreloadTargetView], ptr addrspace(4) @sim_accel_eval_preload_target_views_device, i32 0, i32 %index\n"
    )
    lines.append(
        "  %target = load %SimAccelPreloadTargetView, ptr addrspace(4) %target_ptr, align 8\n"
    )
    lines.append("  %storage = extractvalue %SimAccelPreloadTargetView %target, 0\n")
    lines.append("  %storage_null = icmp eq ptr addrspace(1) %storage, null\n")
    lines.append("  br i1 %storage_null, label %ret_zero, label %check_depth\n\n")
    lines.append("check_depth:\n")
    lines.append("  %depth = extractvalue %SimAccelPreloadTargetView %target, 2\n")
    lines.append("  %word_oob = icmp uge i32 %word_index, %depth\n")
    lines.append("  br i1 %word_oob, label %ret_zero, label %compute_idx\n\n")
    lines.append("compute_idx:\n")
    lines.append("  %depth64 = zext i32 %depth to i64\n")
    lines.append("  %state64 = zext i32 %state_index to i64\n")
    lines.append("  %word64 = zext i32 %word_index to i64\n")
    lines.append("  %base = mul i64 %state64, %depth64\n")
    lines.append("  %linear = add i64 %base, %word64\n")
    lines.append("  %storage_size = extractvalue %SimAccelPreloadTargetView %target, 1\n")
    lines.append("  %linear_oob = icmp uge i64 %linear, %storage_size\n")
    lines.append("  br i1 %linear_oob, label %ret_zero, label %load_word\n\n")
    lines.append("load_word:\n")
    lines.append("  %word_ptr = getelementptr i64, ptr addrspace(1) %storage, i64 %linear\n")
    lines.append("  %word = load i64, ptr addrspace(1) %word_ptr, align 8\n")
    lines.append("  ret i64 %word\n\n")
    lines.append("ret_zero:\n")
    lines.append("  ret i64 0\n")
    lines.append("}\n")
    return "".join(lines)


def _llvm_mask_literal(width: int) -> str:
    width = int(width)
    if width <= 0:
        return "0"
    if width >= 64:
        return str((1 << 64) - 1)
    return str((1 << width) - 1)


def emit_circt_kernel_wrapper_llvm(
    program: dict[str, object],
    source: cuda_fuser.KernelSource,
    *,
    scalar_llvm_ir: str,
    scalar_func_name: str,
    kernel_name: str,
) -> str:
    vars_data = list(program["vars"])
    input_var_idxs = _extract_source_read_var_indices(source)
    output_var_idxs = _extract_source_store_var_indices(source)
    scalar_def = _extract_llvm_function_definition(scalar_llvm_ir, scalar_func_name)
    output_widths = [int(vars_data[var_idx]["width"]) for var_idx in output_var_idxs]
    scalar_ret_ty = _llvm_ir_return_type(output_widths)
    preload_support = _emit_circt_preload_runtime_llvm_support(program)
    lines: list[str] = []
    lines.append("; ModuleID = 'sim_accel_circt_full_all'\n")
    lines.append('target triple = "nvptx64-nvidia-cuda"\n\n')
    if preload_support:
        lines.append(preload_support)
        lines.append("\n")
    lines.append(scalar_def)
    lines.append("\n")
    lines.append("declare i32 @llvm.nvvm.read.ptx.sreg.tid.x()\n")
    lines.append("declare i32 @llvm.nvvm.read.ptx.sreg.ntid.x()\n")
    lines.append("declare i32 @llvm.nvvm.read.ptx.sreg.ctaid.x()\n\n")
    lines.append(f"define void @{kernel_name}(ptr %state_in, ptr %state_out, i32 %nstates) {{\n")
    lines.append("entry:\n")
    lines.append("  %tid_x = call i32 @llvm.nvvm.read.ptx.sreg.tid.x()\n")
    lines.append("  %ntid_x = call i32 @llvm.nvvm.read.ptx.sreg.ntid.x()\n")
    lines.append("  %ctaid_x = call i32 @llvm.nvvm.read.ptx.sreg.ctaid.x()\n")
    lines.append("  %block_base = mul i32 %ctaid_x, %ntid_x\n")
    lines.append("  %tid = add i32 %block_base, %tid_x\n")
    lines.append("  %tid_oob = icmp uge i32 %tid, %nstates\n")
    lines.append("  br i1 %tid_oob, label %exit, label %body\n\n")
    lines.append("body:\n")

    call_args: list[str] = []
    temp_index = 0
    for var_idx in input_var_idxs:
        width = int(vars_data[var_idx]["width"])
        llvm_ty = _llvm_ir_int_type(width)
        slotmul = f"%in_slotmul_{temp_index}"
        idx = f"%in_idx_{temp_index}"
        ptr = f"%in_ptr_{temp_index}"
        load64 = f"%in_load64_{temp_index}"
        lines.append(f"  {slotmul} = mul nuw i32 {var_idx}, %nstates\n")
        lines.append(f"  {idx} = add nuw i32 {slotmul}, %tid\n")
        lines.append(f"  {ptr} = getelementptr i64, ptr %state_in, i32 {idx}\n")
        lines.append(f"  {load64} = load i64, ptr {ptr}, align 8\n")
        value_name = load64
        if width < 64:
            masked = f"%in_masked_{temp_index}"
            narrowed = f"%in_arg_{temp_index}"
            lines.append(f"  {masked} = and i64 {load64}, {_llvm_mask_literal(width)}\n")
            lines.append(f"  {narrowed} = trunc i64 {masked} to {llvm_ty}\n")
            value_name = narrowed
        call_args.append(f"{llvm_ty} {value_name}")
        temp_index += 1
    call_args.append("i32 %tid")

    if output_widths:
        call_name = "%call"
        lines.append(
            f"  {call_name} = call {scalar_ret_ty} @{scalar_func_name}({', '.join(call_args)})\n"
        )
        for out_index, var_idx in enumerate(output_var_idxs):
            width = int(vars_data[var_idx]["width"])
            llvm_ty = _llvm_ir_int_type(width)
            if len(output_widths) == 1:
                out_value = call_name
            else:
                out_value = f"%out_extract_{out_index}"
                lines.append(
                    f"  {out_value} = extractvalue {scalar_ret_ty} {call_name}, {out_index}\n"
                )
            zext = out_value
            if width < 64:
                widened = f"%out_zext_{out_index}"
                lines.append(f"  {widened} = zext {llvm_ty} {out_value} to i64\n")
                zext = widened
            slotmul = f"%out_slotmul_{out_index}"
            idx = f"%out_idx_{out_index}"
            ptr = f"%out_ptr_{out_index}"
            lines.append(f"  {slotmul} = mul nuw i32 {var_idx}, %nstates\n")
            lines.append(f"  {idx} = add nuw i32 {slotmul}, %tid\n")
            lines.append(f"  {ptr} = getelementptr i64, ptr %state_out, i32 {idx}\n")
            lines.append(f"  store i64 {zext}, ptr {ptr}, align 8\n")

    lines.append("  br label %exit\n\n")
    lines.append("exit:\n")
    lines.append("  ret void\n")
    lines.append("}\n\n")
    lines.append("!nvvm.annotations = !{!0}\n")
    lines.append(f"!0 = !{{ptr @{kernel_name}, !\"kernel\", i32 1}}\n")
    return "".join(lines)


