def _mask_value(width: int) -> int:
    if width <= 0:
        return 0
    if width >= 64:
        return 0xFFFFFFFFFFFFFFFF
    return (1 << width) - 1


def _mask_literal(width: int) -> str:
    return f"0x{_mask_value(width):x}ull"


def _mask_wrap(expr: str, width: int) -> str:
    if width >= 64:
        return f"({expr})"
    return f"(({expr}) & {_mask_literal(width)})"


def _unsigned_literal(value: int) -> str:
    return f"0x{value:x}ull"


def _signed_value(expr: str, width: int) -> str:
    return f"(sim_accel_sign_extend_u64(({expr}), {width}u))"


def _c_string(value: str) -> str:
    return json.dumps(value)


def _emit_array_decl(
    ctype: str,
    name: str,
    values: list[str],
    *,
    fallback: str,
) -> str:
    data = values if values else [fallback]
    joined = ", ".join(data)
    return f"static const {ctype} {name}[{len(data)}] = {{{joined}}};\n"


def _render_declaration_cpu(op: cuda_fuser.DeclarationOp) -> str:
    if isinstance(op, cuda_fuser.LoadOp):
        return (
            f"        uint64_t {op.target} = sim_accel_apply_mask_u64("
            f"{op.source_buffer}[{op.index_expr}], {op.width_bits}u);  // {op.comment}"
        )
    return f"        uint64_t {op.target} = {op.value_expr};  // {op.comment}"


def _render_assignment_cpu(op: cuda_fuser.AssignOp) -> str:
    return f"        {op.target} = {op.expr};  // {op.comment}"


def _render_store_cpu(op: cuda_fuser.StoreOp) -> str:
    return (
        f"        state_out[{op.index_expr}] = "
        f"sim_accel_apply_mask_u64({op.value_expr}, {op.width_bits}u);"
    )


