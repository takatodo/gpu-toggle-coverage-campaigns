def _ordered_unique_uint32(values: object) -> list[int]:
    if not isinstance(values, list):
        return []
    ordered: list[int] = []
    seen: set[int] = set()
    for value in values:
        idx = int(value)
        if idx < 0 or idx in seen:
            continue
        seen.add(idx)
        ordered.append(idx)
    return ordered


def _extract_static_var_index(index_expr: str) -> int | None:
    head = index_expr.split("U", 1)[0].strip()
    if head.isdigit():
        return int(head)
    return None


def _extract_source_read_var_indices(source: cuda_fuser.KernelSource) -> list[int]:
    ordered: list[int] = []
    seen: set[int] = set()
    for declaration in source.phase.declarations:
        if not isinstance(declaration, cuda_fuser.LoadOp):
            continue
        if declaration.source_buffer != "state_in":
            continue
        idx = _extract_static_var_index(declaration.index_expr)
        if idx is None or idx in seen:
            continue
        seen.add(idx)
        ordered.append(idx)
    return ordered


def _extract_source_store_var_indices(source: cuda_fuser.KernelSource) -> list[int]:
    ordered: list[int] = []
    seen: set[int] = set()
    for store in source.phase.stores:
        idx = _extract_static_var_index(store.index_expr)
        if idx is None or idx in seen:
            continue
        seen.add(idx)
        ordered.append(idx)
    return ordered


def _select_synthetic_owner_hint(program: dict[str, object]) -> str:
    cluster_hints: dict[int, str] = {}
    hints: list[str] = []
    for cluster in program.get("approx_regcut_clusters", []):
        if not isinstance(cluster, dict):
            continue
        idx = int(cluster.get("cluster_idx", -1))
        hint = str(cluster.get("hybrid_owner_hint", ""))
        if idx >= 0 and hint:
            cluster_hints[idx] = hint
            hints.append(hint)

    hybrid_analysis = program.get("hybrid_cluster_analysis", {})
    if isinstance(hybrid_analysis, dict):
        for cluster_idx in _ordered_unique_uint32(hybrid_analysis.get("cluster_topo_order")):
            hint = cluster_hints.get(cluster_idx)
            if hint:
                return hint

    if not hints:
        return "gpu_candidate"

    priority = {"gpu_candidate": 0, "cpu_boundary_heavy": 1, "cpu_only_blocked": 2}
    counts = Counter(hints)
    return min(
        counts,
        key=lambda hint: (-counts[hint], priority.get(hint, 99), hint),
    )


def _build_synthetic_hybrid_metadata(
    program: dict[str, object],
    source: cuda_fuser.KernelSource,
    input_var_idxs: list[int],
    output_var_idxs: list[int],
) -> dict[str, object]:
    vars_data = list(program["vars"])
    assigns = list(program.get("assigns", []))
    comm_buffers = program.get("comm_buffers", {})

    read_var_idxs: list[int] = []
    write_var_idxs: list[int] = []
    if isinstance(comm_buffers, dict):
        read_var_idxs = _ordered_unique_uint32(comm_buffers.get("cpu_to_gpu_var_idxs"))
        write_var_idxs = _ordered_unique_uint32(comm_buffers.get("gpu_to_cpu_var_idxs"))
    if not read_var_idxs:
        read_var_idxs = _extract_source_read_var_indices(source)
    if not read_var_idxs:
        read_var_idxs = input_var_idxs.copy()
    if not write_var_idxs:
        write_var_idxs = output_var_idxs.copy()
    if not write_var_idxs:
        write_var_idxs = _extract_source_store_var_indices(source)

    input_signature_bits = 0
    for var_idx in read_var_idxs:
        if 0 <= var_idx < len(vars_data):
            input_signature_bits += int(vars_data[var_idx]["width"])

    has_work = bool(assigns)
    return {
        "has_work": has_work,
        "assign_count": len(assigns),
        "read_var_idxs": read_var_idxs,
        "read_from_output_var_idxs": [],
        "write_var_idxs": write_var_idxs,
        "input_signature_bits": input_signature_bits,
        "owner_hint": _select_synthetic_owner_hint(program) if has_work else "",
    }


def _bool_flag(value: object) -> str:
    return "1" if bool(value) else "0"


def _slot_value(value: object) -> str:
    return "-" if value is None else str(int(value))


def _sanitize_hierarchy_key(label: str) -> str:
    out = "".join(ch.lower() if ch.isalnum() else "_" for ch in label)
    out = out.strip("_")
    return out or "top"


def _expr_kind_counts_summary(expr_kind_counts: object) -> str:
    if not isinstance(expr_kind_counts, dict):
        return ""
    parts: list[str] = []
    for kind, count in expr_kind_counts.items():
        count_int = int(count)
        if count_int <= 0:
            continue
        parts.append(f"{kind}={count_int}")
    return ";".join(parts)


def _sorted_preload_targets(program: dict[str, object]) -> list[dict[str, object]]:
    return sorted(
        (target for target in program.get("preload_targets", []) if isinstance(target, dict)),
        key=lambda target: (str(target.get("target_path", "")), str(target.get("name", ""))),
    )


def _sorted_preload_elements(target: dict[str, object]) -> list[dict[str, object]]:
    return sorted(
        (element for element in target.get("elements", []) if isinstance(element, dict)),
        key=lambda element: (
            int(element.get("index", 0)),
            int(element.get("offset", 0)),
            str(element.get("var_name", "")),
        ),
    )


def _select_primary_cluster(program: dict[str, object]) -> dict[str, object] | None:
    clusters = {
        int(cluster.get("cluster_idx", -1)): cluster
        for cluster in program.get("approx_regcut_clusters", [])
        if isinstance(cluster, dict) and int(cluster.get("cluster_idx", -1)) >= 0
    }
    hybrid_analysis = program.get("hybrid_cluster_analysis", {})
    if isinstance(hybrid_analysis, dict):
        for cluster_idx in _ordered_unique_uint32(hybrid_analysis.get("cluster_topo_order")):
            if cluster_idx in clusters:
                return clusters[cluster_idx]
    if not clusters:
        return None
    return clusters[min(clusters)]


def _select_synthetic_hierarchy(
    program: dict[str, object],
    vars_data: list[dict[str, object]],
    synthetic: dict[str, object],
) -> tuple[str, str, int]:
    primary_cluster = _select_primary_cluster(program)
    if primary_cluster is not None:
        cluster_label = str(primary_cluster.get("dominant_hierarchy", ""))
        partition_label = cluster_label or "<top>"
        unique_hierarchy_count = int(primary_cluster.get("unique_hierarchy_count", 0))
        if unique_hierarchy_count > 0:
            return partition_label, cluster_label, unique_hierarchy_count

    hierarchy_counts: Counter[str] = Counter()
    for var_idx in _ordered_unique_uint32(
        list(synthetic.get("read_var_idxs", [])) + list(synthetic.get("write_var_idxs", []))
    ):
        if 0 <= var_idx < len(vars_data):
            hierarchy_counts[str(vars_data[var_idx].get("hierarchy", ""))] += 1
    if hierarchy_counts:
        cluster_label = max(
            hierarchy_counts,
            key=lambda label: (hierarchy_counts[label], label != "", label),
        )
        partition_label = cluster_label or "<top>"
        non_empty = {label for label in hierarchy_counts if label}
        unique_hierarchy_count = len(non_empty) or 1
        return partition_label, cluster_label, unique_hierarchy_count
    return "<top>", "", 1


